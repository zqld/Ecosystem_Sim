package org.broker;

import java.awt.*;

public class Plant {
    // Поля класса
    private int x, y;  // Координаты растения на экране (положение в двумерном мире)
    private int health; // Здоровье растения

    // Конструктор класса, инициализирует растение с заданными координатами и начальным здоровьем
    public Plant(int x, int y) {
        this.x = x;    // Устанавливаем начальную позицию растения по оси X
        this.y = y;    // Устанавливаем начальную позицию растения по оси Y
        this.health = 100;  // Задаем начальное значение здоровья растения
    }

    // Метод обновления состояния растения
    public void update() {
        // Растение восстанавливает здоровье на 1 каждый раз, когда вызывается update()
        health += 1;
        // Здесь можно добавить логику для уменьшения здоровья растения
        // Например, если его съедают травоядные или оно не получает достаточно воды и света.
    }

    // Метод отображения растения на экране
    public void draw(Graphics g) {
        g.setColor(Color.GREEN); // Устанавливаем цвет для растения (зеленый)
        g.fillRect(x * 10, y * 10, 10, 10); // Рисуем растение как зеленый квадрат
        // Умножение координат на 10 позволяет масштабировать растение, чтобы оно отображалось крупнее
    }

    // Метод проверки, живо ли растение
    public boolean isAlive() {
        // Растение считается живым, если его здоровье больше 0
        return health > 0;
    }

    // Геттеры для получения координат растения
    public int getX() {
        return x;  // Возвращаем координату X растения
    }

    public int getY() {
        return y;  // Возвращаем координату Y растения
    }
}
